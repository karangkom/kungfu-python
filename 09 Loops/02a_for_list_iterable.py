# A for loop is used for iterating over a sequence (that is either a list, a tuple, a dictionary, a set, or a string)

# List as iterable
idFoods1 = ['bakso', 'nasi padang', 'soto', 'coto makassar']

for i in idFoods1:
    print(i)

